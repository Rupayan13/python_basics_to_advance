name = "Rupayan0123456"

print(len(name))

print(name[0:2])  # goes from 0 to 2 -1 i.e 0 to 1
print(name[2:-3])  # goes from 2 to 14 - 3 -1 i.e 2 to 11

# print(name[0:10:n]) #skip n-1 characters
print(name[0:10:1])  # skip 0 character
print(name[0:10:2])  # skip 1 characters

print(name[:4])  # replace the first empty number with 0
print(name[4:])  # replace the last empty number with len(name)
